package com.bigmarker.client.deskshare.common;

public final class Dimension {

	private final int width;

	private final int height;
	
	public Dimension(int width, int height) {
		this.width = width;
		this.height = height;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}
	
}
